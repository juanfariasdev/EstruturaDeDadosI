     void mostra(){
        // pilha principal
       listPilha.setText("");
       for(Pallet p: pilha)
          listPilha.append(p.toString()+"\n");
       
       // mostrando a pilha Aux
       listAux.setText("");
       for(Pallet p: paux)
          listAux.append(p.toString()+"\n");
      
      if(pilha.isEmpty())
          palletTopo.setText("Topo: Vazio");
      else
       palletTopo.setText("Topo: "+pilha.peek());
       
    }
    
    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        Pallet p = new Pallet();    
        while(!pilha.isEmpty()){
            p = pilha.peek();
            
            if(p.getProduto().equals(txtProduto.getText())){
                var valueProduct = Integer.parseInt(txtQtd.getText());
                var productQtd = p.getQtd();
                if(valueProduct >= productQtd){
                    // Testar se a quantidade Ã© o que quero remover
                    // Se for > = pop apenas - fala que removeu tudo
                    p = pilha.pop();
                    JOptionPane.showMessageDialog(null, "Removido '"+ txtProduto.getText() +"' por completo com sucesso!");
                    mostra();
                }
                else
                {//se for < = decrementa do p.getQtd a quantidade da caixa de texto
                    // e devolve - push(p)
                    p = pilha.pop();
                    p.setQtd(productQtd - valueProduct);
                    pilha.push(p);
                    JOptionPane.showMessageDialog(null, "Quantidade de " + valueProduct + " produtos removidos!");
                    mostra();
                }
                //mostra();
                break;
            }// fim if equals
            else{
                JOptionPane.showMessageDialog(null, "Movendo para auxiliar:"+p.getProduto() );
                paux.push(pilha.pop());
                mostra();
            }// fim else remove
        }// fim while isEmpty

        // devolve da aux para principal
        while(!paux.isEmpty()){
            JOptionPane.showMessageDialog(null, "Movendo para principal");
            pilha.push(paux.pop());
            mostra();
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

